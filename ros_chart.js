function make2DScatterPlot(tit, loc, datapoints){
    var locId = '#' + loc;
$(function () {
    $(locId).highcharts({
        chart: {
            type: 'scatter',
            zoomType: 'xy',
	    events: {
              load: function() {
                var series = this.series[0];
                setInterval(function() {
                  series.setData(datapoints, true, true);
                }, 2000);
             }
           }
        },
        title: {
            text: tit
        },
        xAxis: {
            title: {
                enabled: true,
                text: 'X (relative)'
            },
            startOnTick: true,
            endOnTick: true,
            showLastLabel: true,
            gridLineWidth: 0
        },
        yAxis: {
            title: {
                enabled: true,
                text: 'Y (relative)'
            },
            gridLineWidth: 0
        },
        legend: {
            layout: 'vertical',
            align: 'left',
            verticalAlign: 'top',
            x: 100,
            y: 70,
            floating: true,
            backgroundColor: (Highcharts.theme && Highcharts.theme.legendBackgroundColor) || '#FFFFFF',
            borderWidth: 1
        },
        plotOptions: {
            scatter: {
                marker: {
                    radius: 5,
                    states: {
                        hover: {
                            enabled: false,
                            lineColor: 'rgb(100,100,100)'
                        }
                    }
                },
                states: {
                    hover: {
                        marker: {
                            enabled: false
                        }
                    }
                },
                //this is the important part
                tooltip: {
                    headerFormat: '<b>{series.name}</b><br>',
                    pointFormat: '{point.x} cm, {point.y} kg'
                }
            }
        },
        series: [{
            color: 'rgba(223, 83, 83, .5)',
            data: datapoints }]
    });
});
};






